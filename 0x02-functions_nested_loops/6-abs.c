/**
 * _abs - a function that computes the absolute value of an integer.
 * @n: integer to be computed
 * Description: abs number is relative to zero
 * Return: absolute value
 */
int _abs(int n)
{
	int number;

	if (n >= 0)
	{
	number = n / 1;
	}
	else
	{
	number = n / -1;
	}
	return (number);
}

